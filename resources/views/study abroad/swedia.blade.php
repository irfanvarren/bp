           <h1>Pendidikan di Swedia</h1>
          <p>
						Sistem pendidikan tinggi di Swedia menggunakan kredit högskolepoäng (higher education credits atau kredit pendidikan tinggi), yang sesuai dengan European Credit Transfer and Accumulation System (ECTS – Sistem Transfer dan Akumulasi Kredit Eropa). Satu tahun kuliah purna waktu senilai dengan 60 ECTS.
</p><p>
Program sarjana S1 atau Bachelor Degree di Swedia yang menawarkan program berbahasa Inggris memiliki jumlah yang sangat terbatas. Universitas-universitas di Swedia menawarkan dua pilihan gelar, yakni the högskoleexamen (ujian sekolah tinggi) yang mempersyaratkan 2 tahun kuliah dan bernilai 120 ECTS; dan kandidatexamen (ujian sarjana) untuk memperoleh gelar bachelor degree, yang memerlukan 3 tahun kuliah dengan jumlah kredit 180 ECTS.
</p><p>
Di program Master Degree, mahasiswa akan mengikuti masterexamen yang merupakan 2 tahun program kuliah master bernilai 120 kredit ECTS; atau magisterexamen, program gelar master 1 tahun bernilai 60 kredit ECTS. Apabila Anda berpikir untuk kuliah menuju gelar S3 (PhD) di Swedia, mungkin akan bermanfaat untuk menyelesaikan gelar master di Swedia terlebih dahulu. Persaingan untuk masuk program PhD dan penelitian cukup ketat, dan Anda memerlukan dedikasi penuh pada mata kuliah pilihan Anda. Anda diharapkan untuk menghasilkan penelitian dan pekerjaan asli atas prakarsa Anda sendiri.

